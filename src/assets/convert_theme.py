def convert_theme(theme_el):
    """Convert Doom theme to JSON"""
    theme_string = theme_el[1]
    theme_dict = {}
    theme_list = theme_string.split("\n")
    # strip whitespace
    theme_list = [colorline.strip() for colorline in theme_list if colorline]
    for colorline in theme_list:
        this_color = colorline.split()
        if len(this_color) <= 2:
            # skip irregular rows
            continue
        hex_string = this_color[1][3:10]  # string representing hex code in "default" column
        color_name = this_color[0][1:]
        theme_dict[color_name] = hex_string
    with open(f"{theme_el[0]}.json", 'w') as j:
        json.dump(theme_dict, j)

def convert_all_themes(doom_list):
    """Convert list of Doom themes to JSON"""
    for theme in doom_list:
        convert_theme(theme)
